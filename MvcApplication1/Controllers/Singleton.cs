namespace MvcApplication1.Models
{
    public class Singleton
    {
        private static Singleton instance;
        public string user { get; set; }

        private Singleton() { }

        public static Singleton Instance
        {
            get
            {
                if (instance == null)
                {
                    instance = new Singleton();
                }
                return instance;
            }
        }        
    }
